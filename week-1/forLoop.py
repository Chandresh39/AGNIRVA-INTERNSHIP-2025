# Python script to calculate and print the sum of numbers from 1 to 10

# Initialize a variable to store the sum
total_sum = 0

# Use a for loop to go through each number from 1 to 10
for i in range(1, 11):
    # Add the current number to the total sum
    total_sum += i

# Print the total sum after the loop is finished
print("The sum of numbers from 1 to 10 is:", total_sum)