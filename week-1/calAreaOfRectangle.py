# Python script to calculate the area of a rectangle based on user input

# Prompt the user to enter the length and convert it to a float
length = float(input("Enter the length of the rectangle: "))

# Prompt the user to enter the width and convert it to a float
width = float(input("Enter the width of the rectangle: "))

# Calculate the area
area = length * width

# Print the result
print("The area of the rectangle is:", area)